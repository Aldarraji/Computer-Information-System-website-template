<h1>Degrees and Certificates</h1>
<br>
<h3>Associate in Arts or Associate in Science Degrees</h3>
<p>These degrees are awarded upon the completion of a curriculum which has been designed to parallel the first two years of a four-year college or university program. Thus, these degrees enable the student to transfer toward a Bachelor of Arts or Bachelor of Science degree.</p><br>

<h3>Associate in Applied Science</h3>
<p>This degree is awarded upon the completion of one of the technical or health occupations curricula. These curricula are designed to prepare the student to enter a career directly upon completion of the program.</p><br>

<h3>Certificate of Completion</h3>
<p>A Certificate of Completion in designated technical and health occupations areas are conferred on students who complete the prescribed curriculum.</p><br>

<h3>Marketable Skills Certificate</h3>
<p>A Marketable Skills Certificate is awarded to students who satisfactorily complete prescribed courses.</p><br>

<h3>Completion Time</h3>
<p>Generally, students completing 15 semester credit hours each semester will be able to complete a certificate of completion within one academic year and an associate degree in two academic years.</p><br>

<h3>Multiple Associate Degrees</h3>
<p>Students who wish to pursue an additional associate degree should consult with their academic advisor.
<br>
A student who has received an associate degree from Amarillo College or any other institution of higher education may obtain an additional associate degree in another area. However, students should seek appropriate academic advising before initiating the pursuit of another associate degree.
<br>
For each additional associate degree, a minimum of 18 additional undergraduate semester credit hours must be completed at Amarillo College or any accredited institution.
All courses required by any specific program must be completed.</p><br>

<h3>Declaration of Major</h3>
<p>Students who wish to pursue an associate degree or certificate of completion must declare a major and should consult with their academic advisor. Under the guidelines listed below, a student may pursue a degree or certificate in a major field of study.
<br>
Major refers to a program of study concentrating in a discipline and related disciplines. Within some majors there may be options or areas of specialization. In the following pages “majors” are presented as curriculum plans which outline the requirements for a degree or certificate in the respective “major”. For example there is a “curriculum plan” for each “major” such as Aviation Maintenance Technology. Each curriculum plan sets out the requirements for a major and the respective degree.
 <br>
Students are encouraged to declare a major as soon as a decision is made regarding their preferred field of study. This declaration will aid in proper advising and encourage student success.
A student who changes majors will be required to graduate under the requirements in effect at the time of the change.
 <br>
A student who has been continuously enrolled in a specific degree or certificate major may choose to graduate under the requirements for that same major in a more recent catalog by changing their catalog year. Enrollment in at least one semester or summer session during the selected catalog year must be documented.
 <br>
A student may not declare a major that has been deleted from our curriculum without permission of the academic dean. Prior enrollment while major was active must be documented.
 <br>
Declaration of Major at Amarillo College
 <br>
A student cannot declare a major that is not active at the time of enrollment.</p><br>

<h3>Graduation Under a Particular Catalog</h3>
<p>Catalog graduation requirements are based upon the year and term of entry to Amarillo College. These catalog requirements will remain in effect for up to five years as long as the student registers for at least one semester or term each school year (i.e. 12 month period beginning with the fall semester and ending with the second summer session). If the student does not register at the college for any one of the four terms during the school year, the student’s new graduation requirements will be those in effect for the year and term the student re-enters the college. A student that changes majors will be required to graduate under requirements in effect at the time of the change. Continuously enrolled students who have not graduated within five years must meet the graduation requirements in effect at the time of their next enrollment.</p><br>

<h3>Application for Graduation</h3>
<p>Graduation is an automatic process. If you are in your final term of completing requirements, you will receive a notification email from the Registrar’s Office.  Please check your Amarillo College email account for all official college communication including graduation notification. Degrees are posted to student transcripts at the end of each term.  Diplomas are available 7-10 days after the end of term and may be picked up at the Registrar’s Office.</p><br>

<h3>Commencement</h3>
<p>Amarillo College holds graduation ceremonies at the end of the spring and fall semesters.  Summer graduates are encouraged to participate in one of these ceremonies.  If you plan on attending a graduation ceremony, you can find detailed information regarding the date, time and instructions on commencement procedures at our website:  www.actx.edu/registrar.  Your name will appear on your diploma as it appears on the official college record. Only legal names can appear on the diploma.</p><br>

<h3>Curriculum Plans</h3>
<p>This section of the catalog presents curricula which lead to Certificates of Completion, Associate in Applied Science (AAS), Associate in Arts (AA), and Associate in Science (AS) degrees.
<br>
Each student who declares an intended major will be given a degree plan and advised by the faculty advisor based on the university to which he/she intends to transfer.
<br>
The certificate and AAS curricula requirements are based on the professional judgment of the faculty and advisory committee for each program. The objective of these curricula is to prepare students for entry level positions in the specific occupation. Course substitutions may be made upon the recommendation of the faculty when it is in the best interest of the student and consistent with the integrity of the program.  All exceptions to curriculum requirements must be approved by the appropriate Dean and the Vice President of Academic Affairs.
<br>
The curricula which lead to an AA or AS degree have been designed to parallel the freshman and sophomore requirements at a typical Texas university in the respective major. The General Education component in each curriculum plan is designed to meet the state core curriculum requirement. Other course requirements are based on the intended major and to best prepare a student for a specific university. Students whose needs are not met by the curricula for specific areas may complete an AS degree under the General Studies curriculum.
<br>
Curricula which are based on the Texas Higher Education Coordinating Board’s Fields of Study do not require completion of the 42 credit hour core curriculum. Those curricula are:
<br>
<br>
Criminal Justice - CRIJ.AS
English - ENGL.AA
Music - MUSI.AS</p><br>
